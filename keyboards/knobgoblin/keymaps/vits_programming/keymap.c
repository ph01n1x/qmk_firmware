// Handle the encoder rotation
bool encoder_update_user(uint8_t index, bool clockwise) 
{
    keyevent_timer = timer_read32();

    // Encoder in code mode
    if (encoder_mode == ENCODER_CODE)
    {
        // Top encoder
        if (index == 0) 
        { 
            if (clockwise) 
            {
                tap_code16(C(KC_B));
                tap_code16(C(KC_N));
                target_animation = ANIMATION_DOWN;
            } 
            else 
            {
                tap_code16(C(KC_B));
                tap_code16(C(KC_P));
                target_animation = ANIMATION_UP;
            }
        }
        
        // Bottom encoder
        if (index == 1) 
        {
            if (clockwise) 
            {
            } 
            else 
            {
            }
        }        
    }
    // Encoder in media mode
    else
    {
        // Top encoder
        if (index == 0) 
        { 
            if (clockwise) 
            {
                tap_code(KC_MNXT);
                target_animation = ANIMATION_RIGHT;
            } 
            else 
            {
                tap_code(KC_MPRV);
                target_animation = ANIMATION_LEFT;
            }
        }

        // Bottom encoder
        if (index == 1) 
        {
            if (clockwise) 
            {
                tap_code(KC_VOLU);
                target_animation = ANIMATION_UP;
            } 
            else 
            {
                tap_code(KC_VOLD);
                target_animation = ANIMATION_DOWN;
            }
        }
    }
    
    // Disable keyboard level code.
    return false;
}
#endif

// Handle key events
bool process_record_user(uint16_t keycode, keyrecord_t *record) 
{
    keyevent_timer = timer_read32();

    if (record->event.pressed)
    {
        switch (keycode) 
        {
            case MO(1):
                target_animation = ANIMATION_AWAKE;
                return true;
                
            case C(KC_F4):
            case A(KC_F4):
                target_animation = ANIMATION_KAKI;
                return true;
                
            case KC_LEFT:
            case KC_HOME:
                target_animation = ANIMATION_LEFT;
                return true; 

            case KC_RIGHT:
            case KC_END:
                target_animation = ANIMATION_RIGHT;
                return true; 

            case KC_UP:
            case KC_PGUP:
            case C(KC_HOME):
                target_animation = ANIMATION_UP;
                return true;
                
            case KC_DOWN:
            case KC_PGDN:
            case C(KC_END):
                target_animation = ANIMATION_DOWN;
                return true;
                
            case KC_F3:
                target_animation = ANIMATION_LTOGIL;
                return true;
                
            case KC_F2:
                target_animation = ANIMATION_RTOGIL;
                return true;
                
            case KC_F9:
                target_animation = ANIMATION_KAKI;
                return true;

            case KC_F5:
            case C(S(KC_F5)):
                target_animation = ANIMATION_RIGHT;
                return true;
                
            case KC_F10:
            case KC_F11:
                target_animation = ANIMATION_RTOGIL;
                return true;
                
            case S(KC_F11):
                target_animation = ANIMATION_LTOGIL;
                return true;
                
            case KC_CALC:
                target_animation = ANIMATION_MATI;
                return true;
                
            case KC_PSCR:
                target_animation = ANIMATION_AWAKE;
                return true;

            case KC_MPLY:
                target_animation = ANIMATION_RTOGIL;
                return true;

            case KC_MNXT:
                target_animation = ANIMATION_RIGHT;
                return true;

            case KC_MPRV:
                target_animation = ANIMATION_LEFT;
                return true;

            case KC_MUTE:
                target_animation = ANIMATION_MATI;
                return true;
                
            case KC_VOLU:
                target_animation = ANIMATION_UP;
                return true;
                
            case KC_VOLD:
                target_animation = ANIMATION_DOWN;
                return true;
                
            case KC_WBAK:
                target_animation = ANIMATION_LEFT;
                return true;
            
            case KC_WFWD:
                target_animation = ANIMATION_RIGHT;
                return true;
            
            case KC_BTN1:
                target_animation = ANIMATION_LTOGIL;
                return true;
            
            case KC_BTN2:
                target_animation = ANIMATION_RTOGIL;
                return true;

            case KC_WH_U:
                target_animation = ANIMATION_UP;
                return true;

            case KC_WH_D:
                target_animation = ANIMATION_DOWN;
                return true;
            
            case KC_ENC1:
                if (encoder_mode == ENCODER_CODE)
                {
                    SEND_STRING(SS_LCTL("bt"));
                    target_animation = ANIMATION_AWAKE;
                }
                else
                {
                    tap_code(KC_MPLY);
                    target_animation = ANIMATION_RTOGIL;
                }
                return true;
                
            case KC_ENC2:
                if (encoder_mode == ENCODER_CODE)
                {
                }
                else
                {
                    tap_code(KC_MUTE);
                    target_animation = ANIMATION_MATI;
                }
                return true;

            case KC_ENCT:
                if (encoder_mode == ENCODER_MEDIA)
                {
                    encoder_mode = ENCODER_CODE;
                }
                else
                {
                    encoder_mode = ENCODER_MEDIA;
                }
                target_animation = ANIMATION_AWAKE;
                break;
                
            default:
                target_animation = rand() % 9 + 2;
                return true; 
        }
    }
    
    return true;
}
      
